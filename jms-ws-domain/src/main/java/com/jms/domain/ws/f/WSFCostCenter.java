package com.jms.domain.ws.f;

public class WSFCostCenter{

	    private Long idCostCenter;
	    private Long companyId;
	    private String companyName;
	    private Long costCenterNo;
	    private String des;
		
	    public Long getIdCostCenter() {
			return idCostCenter;
		}
		public void setIdCostCenter(Long idCostCenter) {
			this.idCostCenter = idCostCenter;
		}
		public Long getCompanyId() {
			return companyId;
		}
		public void setCompanyId(Long companyId) {
			this.companyId = companyId;
		}
		public String getCompanyName() {
			return companyName;
		}
		public void setCompanyName(String companyName) {
			this.companyName = companyName;
		}
		public Long getCostCenterNo() {
			return costCenterNo;
		}
		public void setCostCenterNo(Long costCenterNo) {
			this.costCenterNo = costCenterNo;
		}
		public String getDes() {
			return des;
		}
		public void setDes(String des) {
			this.des = des;
		}

	   
}
