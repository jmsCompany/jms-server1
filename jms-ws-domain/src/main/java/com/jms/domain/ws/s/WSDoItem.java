package com.jms.domain.ws.s;


public class WSDoItem {


	private String pno;
	private String name;
	private String coOrderNo;
	private String qty;
	private String unit;
	private String doQty;
    private String doQtySum;
    private String woNo;
    private String remark;
	public String getPno() {
		return pno;
	}
	public void setPno(String pno) {
		this.pno = pno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCoOrderNo() {
		return coOrderNo;
	}
	public void setCoOrderNo(String coOrderNo) {
		this.coOrderNo = coOrderNo;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getDoQty() {
		return doQty;
	}
	public void setDoQty(String doQty) {
		this.doQty = doQty;
	}
	public String getDoQtySum() {
		return doQtySum;
	}
	public void setDoQtySum(String doQtySum) {
		this.doQtySum = doQtySum;
	}
	public String getWoNo() {
		return woNo;
	}
	public void setWoNo(String woNo) {
		this.woNo = woNo;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
